# Use an arithmetic operator
result = 10 + 5  # Addition
print("Arithmetic operator (10 + 5):", result)

# Use an assignment operator
x = 10
x += 5  # Equivalent to x = x + 5
print("Assignment operator (x += 5):", x)

# Use a comparison operator
comparison = (x == 15)  # Equality check
print("Comparison operator (x == 15):", comparison)

# Use a logical operator
logical = (x > 10 and x < 20)  # Logical AND
print("Logical operator (x > 10 and x < 20):", logical)

# Use an identity operator
a = [1, 2, 3]
b = a
identity = (a is b)  # Identity check
print("Identity operator (a is b):", identity)

# Use a membership operator
membership = (2 in a)  # Membership check
print("Membership operator (2 in a):", membership)

# Use a bitwise operator
bitwise = (5 & 3)  # Bitwise AND
print("Bitwise operator (5 & 3):", bitwise)
