# Condition for True
x = 10
if x > 5:
    print("The condition is True: x is greater than 5")
else:
    print("The condition is False: x is not greater than 5")

# Condition for False
y = 2
if y > 5:
    print("The condition is True: y is greater than 5")
else:
    print("The condition is False: y is not greater than 5")
