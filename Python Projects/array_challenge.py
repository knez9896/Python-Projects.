# Create an array (in Python, we use a list)
my_array = [5, 3, 9, 1, 3, 7, 5]

# Loop through all the elements of the array using a for loop
for element in my_array:
    print(element)

# Use the count() method on an array to count occurrences of a specific element
count_3 = my_array.count(3)
print("Count of 3:", count_3)

# Use the sort() method on the array to sort the elements in ascending order
my_array.sort()
print("Sorted array:", my_array)
