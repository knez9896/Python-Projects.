# Create a tuple and print it
my_tuple = (1, 2, 3, 2, 4)
print("Tuple:", my_tuple)

# Loop through tuple items by using a for loop
for item in my_tuple:
    print("Item:", item)

# Use the count() method on a tuple
count_of_twos = my_tuple.count(2)
print("Count of 2 in tuple:", count_of_twos)
